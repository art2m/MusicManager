        ///     Gets or sets the new song path.
        /// </summary>
        /// <value>The new song path.</value>
        public static string NewSongFilePath { get; set; }

        /// <summary>
        ///     Gets or sets the new name of the song.
        /// </summary>
        /// <value>The new name of the song.</value>
        public static string NewSongTitle { get; set; }

