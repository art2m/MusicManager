        ///     Gets or sets the new name of the genre directory.
        /// </summary>
        /// <value>The new name of the genre directory.</value>
        public static string NewGenreDirectoryName { get; set; }

        /// <summary>
        ///     Gets or sets the new genre directory path.
        /// </summary>
        /// <value>The new genre directory path.</value>
        public static string NewGenreDirectoryPath { get; set; }

