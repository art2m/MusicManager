{
    /// <summary>
    ///     public class SongRecordProp Holds properties for the song record.
    /// </summary>
    public static class SongRecordProperties
    {
        /// <summary>
        ///     Gets or sets the name of the album.
        /// </summary>
        /// <value>The name of the album.</value>
        public static string AlbumDirectoryName { get; set; }

        /// <summary>
        ///     Gets or sets the album path.
        /// </summary>
        /// <value>The album path.</value>
        public static string AlbumDirectoryPath { get; set; }

        /// <summary>
        ///     Gets or sets the name of the artist.
        /// </summary>
        /// <value>The name of the artist.</value>
        public static string ArtistDirectoryName { get; set; }

        /// <summary>
        ///     Gets or sets the artist path.
        /// </summary>
        /// <value>The artist path.</value>
        public static string ArtistDirectoryPath { get; set; }

