        ///     Gets or sets the new name of the album.
        /// </summary>
        /// <value>The new name of the album.</value>
        public static string NewAlbumDirectoryName { get; set; }

        /// <summary>
        ///     Gets or sets the new album path.
        /// </summary>
        /// <value>The new album path.</value>
        public static string NewAlbumDirectoryPath { get; set; }

