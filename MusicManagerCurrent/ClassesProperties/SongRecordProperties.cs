        /// </summary>
        /// <value>The create new genre users list.</value>
        public static bool CreateNewGenreList { get; set; }

        /// <summary>
        ///     Gets or sets the name of the genre directory.
        /// </summary>
        /// <value>The name of the genre directory.</value>
        public static string GenreDirectoryName { get; set; }

        /// <summary>
        ///     Gets or sets the genre directory path.
        /// </summary>
        /// <value>The genre directory path.</value>
        public static string GenreDirectoryPath { get; set; }

