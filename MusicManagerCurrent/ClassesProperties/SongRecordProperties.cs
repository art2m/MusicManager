        ///     Gets or sets the name of the top level music directory.
        /// </summary>
        /// <value>The name of the top level music directory.</value>
        public static string MusicDirectoryName { get; set; }

        /// <summary>
        ///     Gets or sets the top level music directory path.
        /// </summary>
        /// <value>The top level music directory path.</value>
        public static string MusicDirectoryPath { get; set; }

